import 'package:flutter/material.dart';

class Details {

  String name;
  int price;
  String info;
  String image;
  String rate;
  bool like;
  bool cart;
  int id;

  Details({
    required this.name,
    required this.price,
    required this.info,
    required this.image,
    required this.rate,
    required this.like,
    required this.cart,
    required this.id,
  });
}

